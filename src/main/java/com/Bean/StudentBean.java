package com.Bean;


public class StudentBean {

	private String studentName;
	private String playingType;
	private String food;
	private String coolDrink;
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getPlayingType() {
		return playingType;
	}
	public void setPlayingType(String playingType) {
		this.playingType = playingType;
	}
	public String getFood() {
		return food;
	}
	public void setFood(String food) {
		this.food = food;
	}
	public String getCoolDrink() {
		return coolDrink;
	}
	public void setCoolDrink(String coolDrink) {
		this.coolDrink = coolDrink;
	}
	
}
